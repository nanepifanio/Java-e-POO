    private void btnVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerActionPerformed
        // TODO add your handling code here:
            int a = sliA.getValue();
            int b = sliB.getValue();
            int c = sliC.getValue();
            boolean exist = (a > Math.abs(b-c) && a < b + c) && (b > Math.abs(a-c) && b < a + c) && (c > Math.abs(a-b ) && c < a + b);
            String verif = (exist)?"Forma um tri√¢ngulo":"N√£o forma um tri√¢ngulo";
            lblTriOuNao.setText(verif);
            if  ((a != b && b != c && c != a) && (exist)) {
                lblTipoTri.setText("Escaleno");
            } else if (((a == b && a != c) || (b == c && b != a) || (c == a && c !=b)) && (exist)) {
                lblTipoTri.setText("Is√≥sceles");
            } else if ((a == b && a == c) && (exist)) {
                lblTipoTri.setText("Equil√°tero");
            } else {
                lblTipoTri.setText("---");
            }
            panResultado.setVisible(true);
    }//GEN-LAST:event_btnVerActionPerformed

